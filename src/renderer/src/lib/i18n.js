/**
 * Tiny i18n: one `t(key, vars)` helper + plain dictionaries. English is the
 * source and the fallback, so a missing key (a not-yet-translated string)
 * shows English instead of breaking. Keep it light — no library for ~150
 * strings. The AI conversation is already localized by the model itself.
 */

export const UI_LANGUAGES = [
    { value: 'en', label: 'English', flag: '🇺🇸', img: enFlag },
    { value: 'pt', label: 'Português', flag: '🇧🇷', img: ptFlag },
    { value: 'es', label: 'Español', flag: '🇪🇸', img: esFlag },
];

/** Map an OS locale like "pt-BR" to one of our supported UI languages. */
export function localeToLang(locale) {
    const two = String(locale || '').slice(0, 2).toLowerCase();
    return UI_LANGUAGES.some((l) => l.value === two) ? two : 'en';
}

